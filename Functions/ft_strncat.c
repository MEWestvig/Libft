char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *s1i;

	s1i = s1;
	while (*s1i++)
		;
	while (n > 0)
	{
		*s1i++ = *s2++;
		n--;
	}
	*s1i = '\0';
	return (s1);
}
