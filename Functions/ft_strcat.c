#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *s1i;

	s1i = s1;
	while (*s1i++)
		;
	while (*s2)
		*s1i++ = *s2++;
	*s1i = '\0';
	return (s1);
}
